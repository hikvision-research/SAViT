"""
Adapt all models from paper into new models for pruning and deploying
"""
import torch
import torch.nn as nn
from functools import partial

from timm.models.vision_transformer import _cfg
from .modules import VisionTransformer, VisionTransformer_deploy
from timm.models.registry import register_model

__all__ = [
    'deit_tiny_patch16_224', 'deit_tiny_patch16_224_deploy',
    'deit_small_patch16_224', 'deit_small_patch16_224_deploy',
    'deit_base_patch16_224', 'deit_base_patch16_224_deploy'
]

@register_model
def deit_tiny_patch16_224(pretrained=False, **kwargs):
    model = VisionTransformer(
        patch_size=16, embed_dim=192, depth=12, num_heads=3, mlp_ratio=4, qkv_bias=True,
        norm_layer=partial(nn.LayerNorm, eps=1e-6), **kwargs)
    model.default_cfg = _cfg()
    if pretrained:
        checkpoint = torch.hub.load_state_dict_from_url(
            url="https://dl.fbaipublicfiles.com/deit/deit_tiny_patch16_224-a1311bcf.pth",
            map_location="cpu", check_hash=True
        )
        model.load_state_dict(checkpoint["model"])
    return model

